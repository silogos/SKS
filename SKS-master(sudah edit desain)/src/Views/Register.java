    private void jPanel_simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel_simpanMouseClicked
        // TODO add your handling code here:
        Kata_sandi = jPasswordField_katasandi.getText();
        konfir_katasandi = jPasswordField_konfirkatasandi.getText();
        PanjangNama = jTextField_username.getText().length();
        panjanglaki_laki = jCheckBox_laki_laki.getText().length();
        panjangperempuan = jCheckBox_perempuan.getText().length();
        Panjangemail = jTextField_email.getText().length();
        
        try {
            if (konfir_katasandi.equals(Kata_sandi) && PanjangNama >= 1 && panjanglaki_laki >=1 && panjangperempuan >= 1 && Panjangemail >=1 ) {
                int executeUpdate = stm.executeUpdate("insert into hasil VALUES ("
                        +"'"+Id_daftar+","
                                +"'"+jTextField_username.getText()+"',"
                                        +"'"+jCheckBox_laki_laki.getText()+"',"
                                                +"'"+jCheckBox_perempuan.getText()+"',"
                                                        +"'"+jTextField_email.getText()+"',"
                                                                +"'"+String.copyValueOf(jPasswordField_katasandi.getPassword())+"',"
                                                                        +"'"+String.copyValueOf(jPasswordField_konfirkatasandi.getPassword())+"',"
                                                                                
                                                                                + "')");
                
            }
            {
             JOptionPane.showConfirmDialog(this, "Data Berhasil Disimpan");
            } bersih();
               
                  
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR, Data Gagal Disimpan");
        }
    
        
        
        
    }//GEN-LAST:event_jPanel_simpanMouseClicked

